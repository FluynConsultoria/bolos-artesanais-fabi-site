// Fun칞칚o para processar o formul치rio de login ou cadastro
function handleAuth(event) {
    event.preventDefault(); // Impede o envio do formul치rio

    const username = document.getElementById("username").value;
    const password = document.getElementById("password").value;

    // Verifica se o bot칚o clicado 칠 para login ou cadastro
    const submitButton = event.target.querySelector("button");
    if (submitButton.textContent === "Entrar") {
        loginUser(username, password); // Chama a fun칞칚o de login
    } else {
        registerUser(username, password); // Chama a fun칞칚o de cadastro
    }
}

